// bitwise & AND operator
var a = 5;   // 5 in bit value  0101
var b = 1;   //1 in bit value  0001
var c = (a&b);
console.log(c); //1

// bitwise  | Or operator
var a = 5;   // 5 in bit value  0101
var b = 1;   //1 in bit value  0001
var c = (a|b);
console.log(c); //5

// bitwise  ~ NOT operator
var a = 5;   //  inverse of 5
var b = ~a;
console.log(b); //-6